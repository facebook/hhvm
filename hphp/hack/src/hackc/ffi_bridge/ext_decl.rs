pub fn get_file_typedefs(parsed_file: &ParsedFileHolder, name: &str) -> Vec<ExtDeclTypeDef> {
    match parsed_file {
        ParsedFileHolder::O(file) => file
            .decls
            .typedefs()
            .filter(|(cname, _)| name.is_empty() || name == strip_global_ns(cname))
            .map(|(cname, decl)| {
                let (visibility, type_) = match &decl.type_assignment {
                    TypedefTypeAssignment::SimpleTypeDef(vis, hint) => {
                        (enum_typedef_visibility(vis), extract_type_name(hint))
                    }
                    TypedefTypeAssignment::CaseType(variant, variants) => {
                        let mut hints;
                        let ty_ = if variants.is_empty() {
                            variant.0.1.clone()
                        } else {
                            hints = variants.iter().map(|v| v.0.clone()).collect::<Vec<_>>();
                            hints.insert(0, variant.0.clone());
                            Box::new(Ty_::Tunion(hints))
                        };
                        (
                            String::from("case_type"),
                            extract_type_name(&Ty(
                                oxidized::typing_reason::Reason::NoReason, // the position is ignored when printing the type
                                ty_,
                            )),
                        )
                    }
                };
                ExtDeclTypeDef {
                    name: fmt_type(cname),
                    module: id_or_empty(decl.module.as_ref()),
                    visibility,
                    tparams: get_typed_params(&decl.tparams),
                    as_constraint: extract_type_name_opt(decl.as_constraint.as_ref()),
                    super_constraint: extract_type_name_opt(decl.super_constraint.as_ref()),
                    type_,
                    is_ctx: decl.is_ctx,
                    attributes: get_attributes(&decl.attributes, ""),
                    internal: decl.internal,
                    docs_url: str_or_empty(decl.docs_url.as_deref()),
                }
            })
            .collect(),
        ParsedFileHolder::Obr(file, _) => file
            .decls
            .typedefs()
            .filter(|(cname, _)| name.is_empty() || name == strip_global_ns(cname))
            .map(|(cname, decl)| {
                let (visibility, type_) = match decl.type_assignment {
                    TypedefTypeAssignmentObr::SimpleTypeDef((vis, hint)) => {
                        (enum_typedef_visibility(vis), extract_type_name_obr(hint))
                    }
                    TypedefTypeAssignmentObr::CaseType((variant, variants)) => {
                        let mut hints;
                        let ty_ = if variants.is_empty() {
                            variant.0.1
                        } else {
                            hints = variants.iter().map(|v| v.0).collect::<Vec<_>>();
                            hints.insert(0, variant.0);
                            Ty_Obr::Tunion(&hints)
                        };
                        (
                            String::from("case_type"),
                            extract_type_name_obr(&TyObr(
                                &typing_reason::Reason::NoReason, // the position is ignored when printing the type
                                ty_,
                            )),
                        )
                    }
                };
                ExtDeclTypeDef {
                    name: fmt_type(cname),
                    module: id_or_empty_obr(decl.module),
                    visibility,
                    tparams: get_typed_params_obr(decl.tparams),
                    as_constraint: extract_type_name_opt_obr(decl.as_constraint),
                    super_constraint: extract_type_name_opt_obr(decl.super_constraint),
                    type_,
                    is_ctx: decl.is_ctx,
                    attributes: get_attributes_obr(decl.attributes, ""),
                    internal: decl.internal,
                    docs_url: str_or_empty(decl.docs_url),
                }
            })
            .collect(),
    }
}

pub fn get_type_structure(parsed_file: &ParsedFileHolder, name: &str) -> Vec<ExtDeclTypeStructure> {
    match parsed_file {
        ParsedFileHolder::O(file) => file
            .decls
            .typedefs()
            .filter(|(cname, _)| name.is_empty() || name == strip_global_ns(cname))
            .map(|(_cname, decl)| match &decl.type_assignment {
                TypedefTypeAssignment::SimpleTypeDef(_vis, hint) => build_type_structure(hint),
                TypedefTypeAssignment::CaseType(variant, variants) => {
                    let mut hints = variants.iter().map(|v| v.0.clone()).collect::<Vec<_>>();
                    hints.insert(0, variant.0.clone());
                    build_type_structure(&Ty(
                        // the reason is not included in the type structure
                        oxidized::typing_reason::Reason::NoReason,
                        Box::new(Ty_::Tunion(hints)),
                    ))
                }
            })
            .collect(),
        ParsedFileHolder::Obr(file, _) => file
            .decls
            .typedefs()
            .filter(|(cname, _)| name.is_empty() || name == strip_global_ns(cname))
            .map(|(_cname, decl)| match decl.type_assignment {
                TypedefTypeAssignmentObr::SimpleTypeDef((_vis, hint)) => {
                    build_type_structure_obr(hint)
                }
                TypedefTypeAssignmentObr::CaseType((variant, variants)) => {
                    let mut hints = variants.iter().map(|v| v.0).collect::<Vec<_>>();
                    hints.insert(0, variant.0);
                    build_type_structure_obr(&TyObr(
                        // the reason is not included in the type structure
                        &typing_reason::Reason::NoReason,
                        Ty_Obr::Tunion(&hints),
                    ))
                }
            })
            .collect(),
    }
}

pub fn get_file(parsed_file: &ParsedFileHolder) -> ExtDeclFile {
    let (disable_xhp_element_mangling, has_first_pass_parse_errors, mode) = match parsed_file {
        ParsedFileHolder::O(file) => (
            file.disable_xhp_element_mangling,
            file.has_first_pass_parse_errors,
            file.mode,
        ),
        ParsedFileHolder::Obr(file, _) => (
            file.disable_xhp_element_mangling,
            file.has_first_pass_parse_errors,
            file.mode,
        ),
    };
    ExtDeclFile {
        disable_xhp_element_mangling,
        has_first_pass_parse_errors,
        is_strict: mode.is_some_and(|x| x == Mode::Mstrict),
        typedefs: get_file_typedefs(parsed_file, ""),
        functions: get_file_funcs(parsed_file, ""),
        constants: get_file_consts(parsed_file, ""),
        file_attributes: get_file_attributes(parsed_file, ""),
        modules: get_file_modules(parsed_file, ""),
        classes: get_classes(parsed_file),
    }
}

pub fn get_shape_keys(parsed_file: &ParsedFileHolder, name: &str) -> Vec<String> {
    // don't let empty strings pass in, else we fetch all type structures
    if name.is_empty() {
        return vec![];
    }
    let type_structure = get_type_structure(parsed_file, name);
    // if empty, then no matching type for the name
    let Some(type_) = type_structure.first() else {
        return vec![];
    };
    // can't get keys unless it's a shape
    match type_.kind.as_str() {
        "shape" => type_.subtypes.iter().map(|st| st.name.clone()).collect(),
        _ => vec![],
    }
}

