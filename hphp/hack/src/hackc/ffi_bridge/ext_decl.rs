fn build_type_structure(outer_ty: &Ty) -> ExtDeclTypeStructure {
    match &*outer_ty.1 {
        Ty_::Taccess(_)
        | Ty_::Tthis
        | Ty_::Tany(_)
        | Ty_::Tclass(..)
        | Ty_::Tdynamic
        | Ty_::Tgeneric(..)
        | Ty_::Tmixed
        | Ty_::Twildcard
        | Ty_::Tnonnull
        | Ty_::Tprim(_) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("primitive"),
            nullable: false,
            subtypes: vec![],
        },
        Ty_::Tfun(_fun_type) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("function"),
            nullable: false,
            subtypes: vec![],
        },

        // Pseudo Recursive
        Ty_::Toption(ty) => {
            let mut inner = build_type_structure(ty);
            inner.nullable = true;
            inner
        }

        Ty_::Tapply(_id, targs) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("apply"),
            nullable: false,
            subtypes: targs
                .iter()
                .map(build_unnamed_type_structure_subtype)
                .collect(),
        },
        Ty_::Tlike(ty) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("like"),
            nullable: false,
            subtypes: vec![build_unnamed_type_structure_subtype(ty)],
        },
        Ty_::Tshape(ShapeType {
            origin: _,
            unknown_value: _kind,
            fields,
        }) => {
            let shape_fields = fields
                .iter()
                .map(
                    |(shape_field, shape_field_type)| ExtDeclTypeStructureSubType {
                        name: match &shape_field.0 {
                            TshapeFieldName::TSFregexGroup(field_name) => field_name.1.to_string(),
                            TshapeFieldName::TSFlitStr(field_name) => field_name.1.to_string(),
                            TshapeFieldName::TSFclassConst((_pos, classish_name), field_name) => {
                                let cname = fmt_type(classish_name);
                                format!("{}::{}", cname, field_name.1)
                            }
                        },
                        type_: build_type_structure(&shape_field_type.ty),
                        optional: shape_field_type.optional,
                    },
                )
                .collect();

            ExtDeclTypeStructure {
                type_: extract_type_name(outer_ty),
                kind: String::from("shape"),
                nullable: false,
                subtypes: shape_fields,
            }
        }
        Ty_::TvecOrDict(tk, tv) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("refinement"),
            nullable: false,
            subtypes: vec![
                ExtDeclTypeStructureSubType {
                    name: String::from("keys"),
                    optional: false,
                    type_: build_type_structure(tk),
                },
                ExtDeclTypeStructureSubType {
                    name: String::from("values"),
                    optional: false,
                    type_: build_type_structure(tv),
                },
            ],
        },
        Ty_::Ttuple(TupleType { required, extra: _ }) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("tuple"),
            nullable: false,
            subtypes: required
                .iter()
                .map(build_unnamed_type_structure_subtype)
                .collect(),
        },
        Ty_::Tintersection(tys) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("intersection"),
            nullable: false,
            subtypes: tys
                .iter()
                .map(build_unnamed_type_structure_subtype)
                .collect(),
        },
        Ty_::Tunion(tys) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("union"),
            nullable: false,
            subtypes: tys
                .iter()
                .map(build_unnamed_type_structure_subtype)
                .collect(),
        },
        Ty_::Trefinement(root_ty, _class_ref) => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("refinement"),
            nullable: false,
            subtypes: vec![build_unnamed_type_structure_subtype(root_ty)],
        },
        _ => ExtDeclTypeStructure {
            type_: extract_type_name(outer_ty),
            kind: String::from("other"),
            nullable: false,
            subtypes: vec![],
        },
    }
}

fn build_type_structure_obr(outer_ty: &TyObr<'_>) -> ExtDeclTypeStructure {
    match outer_ty.1 {
        Ty_Obr::Taccess(_)
        | Ty_Obr::Tthis
        | Ty_Obr::Tany(_)
        | Ty_Obr::Tclass(_)
        | Ty_Obr::Tdynamic
        | Ty_Obr::Tgeneric(_)
        | Ty_Obr::Tmixed
        | Ty_Obr::Twildcard
        | Ty_Obr::Tnonnull
        | Ty_Obr::Tprim(_) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("primitive"),
            nullable: false,
            subtypes: vec![],
        },
        Ty_Obr::Tfun(_fun_type) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("function"),
            nullable: false,
            subtypes: vec![],
        },

        // Pseudo Recursive
        Ty_Obr::Toption(ty) => {
            let mut inner = build_type_structure_obr(ty);
            inner.nullable = true;
            inner
        }

        Ty_Obr::Tapply(&(_id, targs)) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("apply"),
            nullable: false,
            subtypes: targs
                .iter()
                .map(build_unnamed_type_structure_subtype_obr)
                .collect(),
        },
        Ty_Obr::Tlike(ty) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("like"),
            nullable: false,
            subtypes: vec![build_unnamed_type_structure_subtype_obr(&ty)],
        },
        Ty_Obr::Tshape(&ShapeTypeObr {
            origin: _,
            unknown_value: _kind,
            fields,
        }) => {
            let shape_fields = fields
                .iter()
                .map(
                    |(shape_field, shape_field_type)| ExtDeclTypeStructureSubType {
                        name: match shape_field.0 {
                            TshapeFieldNameObr::TSFregexGroup(field_name) => {
                                field_name.1.to_string()
                            }
                            TshapeFieldNameObr::TSFlitStr(field_name) => field_name.1.to_string(),
                            TshapeFieldNameObr::TSFclassConst((
                                (_pos, classish_name),
                                field_name,
                            )) => {
                                let cname = fmt_type(classish_name);
                                format!("{}::{}", cname, field_name.1)
                            }
                        },
                        type_: build_type_structure_obr(shape_field_type.ty),
                        optional: shape_field_type.optional,
                    },
                )
                .collect();

            ExtDeclTypeStructure {
                type_: extract_type_name_obr(outer_ty),
                kind: String::from("shape"),
                nullable: false,
                subtypes: shape_fields,
            }
        }
        Ty_Obr::TvecOrDict(&(tk, tv)) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("refinement"),
            nullable: false,
            subtypes: vec![
                ExtDeclTypeStructureSubType {
                    name: String::from("keys"),
                    optional: false,
                    type_: build_type_structure_obr(tk),
                },
                ExtDeclTypeStructureSubType {
                    name: String::from("values"),
                    optional: false,
                    type_: build_type_structure_obr(tv),
                },
            ],
        },
        Ty_Obr::Ttuple(&TupleTypeObr { required, extra: _ }) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("tuple"),
            nullable: false,
            subtypes: required
                .iter()
                .map(build_unnamed_type_structure_subtype_obr)
                .collect(),
        },
        Ty_Obr::Tintersection(tys) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("intersection"),
            nullable: false,
            subtypes: tys
                .iter()
                .map(build_unnamed_type_structure_subtype_obr)
                .collect(),
        },
        Ty_Obr::Tunion(tys) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("union"),
            nullable: false,
            subtypes: tys
                .iter()
                .map(build_unnamed_type_structure_subtype_obr)
                .collect(),
        },
        Ty_Obr::Trefinement(&(root_ty, _class_ref)) => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("refinement"),
            nullable: false,
            subtypes: vec![build_unnamed_type_structure_subtype_obr(&root_ty)],
        },
        _ => ExtDeclTypeStructure {
            type_: extract_type_name_obr(outer_ty),
            kind: String::from("other"),
            nullable: false,
            subtypes: vec![],
        },
    }
}

// ========== Types ==========

fn extract_type_name_opt(t: Option<&Ty>) -> String {
    match t {
        Some(ty) => extract_type_name(ty),
        None => String::new(),
    }
}

fn extract_type_name_opt_obr(t: Option<&TyObr<'_>>) -> String {
    match t {
        Some(ty) => extract_type_name_obr(ty),
        None => String::new(),
    }
}

fn extract_type_name(t: &Ty) -> String {
    #[allow(suspicious_double_ref_op)]
    let converted_ty: ty::decl::Ty<BReason> = t.clone().into();
    converted_ty.to_string()
}

fn extract_type_name_obr(t: &TyObr<'_>) -> String {
    #[allow(suspicious_double_ref_op)]
    let converted_ty: ty::decl::Ty<BReason> = (&t).clone().into();
    converted_ty.to_string()
}

fn extract_type_name_vec(arr: &[Ty]) -> Vec<String> {
    arr.iter().map(extract_type_name).collect()
}

fn extract_type_name_vec_obr(arr: &[&TyObr<'_>]) -> Vec<String> {
    arr.iter().map(|t| extract_type_name_obr(t)).collect()
}

fn extract_type_name_cr_vec(arr: &[DeclConstraintRequirement]) -> Vec<String> {
    arr.iter()
        .filter_map(|cr| match cr {
            DeclConstraintRequirement::DCREqual(t) => Some(extract_type_name(t)),
            DeclConstraintRequirement::DCRSubtype(_) => None,
        })
        .collect()
}

fn extract_type_name_cr_vec_obr(arr: &[DeclConstraintRequirementObr<'_>]) -> Vec<String> {
    arr.iter()
        .filter_map(|cr| match cr {
            DeclConstraintRequirementObr::DCREqual(t) => Some(extract_type_name_obr(t)),
            DeclConstraintRequirementObr::DCRSubtype(_) => None,
        })
        .collect()
}

fn fmt_type(original_name: &str) -> String {
    let unqualified = strip_global_ns(original_name);
    match unqualified.rsplit('\\').next() {
        Some(id) if original_name.starts_with('\\') && id.starts_with(':') => {
            // only mangle already qualified xhp ids - avoid mangling string literals
            // containing an xhp name, for example an attribute param ':foo:bar'
            mangle_xhp_id(id.to_string())
        }
        _ => String::from(unqualified),
    }
}

// ========== Simple ==========

fn id_or_empty(x: Option<&Id>) -> String {
    str_or_empty(x.map(|y| y.name()))
}

fn id_or_empty_obr(x: Option<IdObr<'_>>) -> String {
    str_or_empty(x.map(|y| y.name()))
}

fn str_or_empty(x: Option<&str>) -> String {
    x.map_or_else(String::new, |y| y.to_string())
}

