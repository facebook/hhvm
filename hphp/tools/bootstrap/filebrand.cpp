#include "hphp/tools/bootstrap/filebrand.h"

#include <fstream>
#include <string>

namespace HPHP { namespace FileBrander {

void makeInvocationTrace(std::string& invocation_trace,
                         int argc,
                         const char* argv[])
{
  for (int i = 0; i < argc; i++) {
    invocation_trace += "// ";
    if (i > 0) {
      invocation_trace += "  ";
    }
    invocation_trace += argv[i];
    invocation_trace += "\n";
  }
}

void brandOutputFile(std::ostream& out,
                     const char* short_name,
                     const std::string& invocation_trace)
{
  out << "//\n";
  out << "// AUTOMATICALLY GENERATED: DO NOT EDIT\n";
  out << "// @" "generated by " << short_name << "\n";
  out << invocation_trace;
  out << "//\n";
}

}}
